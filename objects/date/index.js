// Date objects = Objects that contain values that represents dates and times
//                Built-in object in JavaScript (These date objects can be changed and formatted) 

const date1 = new Date(2026, 0, 1, 2, 3, 4, 5); // January 1, 2026
console.log(date1);

const date2 = new Date("2026-01-02T12:00:00Z"); // January 2, 2026
console.log(date2);